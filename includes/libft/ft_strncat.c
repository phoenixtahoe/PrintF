#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t x;
	size_t i;

	x = 0;
	i = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while ((s2[x] != '\0') && (x < n))
	{
		s1[i] = s2[x];
		i++;
		x++;
	}
	s1[i] = '\0';
	return (s1);
}
